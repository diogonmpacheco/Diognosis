#!/usr/bin/env node
import { existsSync, readFileSync } from 'fs';
import { resolve } from 'path';

const GENERATED_DATA_MODULES = [
  'data/generatedStats.js',
  'data/generatedEvidenceReviewQueue.js',
  'data/generatedOpenTargetsSnapshot.js',
  'data/generatedOpenTargetsPromotionQueue.js',
  'data/generatedOpenTargetsReviewTargets.js',
  'data/generatedOpenTargetsPgxGapRoadmap.js',
  'data/generatedOpenTargetsMechanisticQueue.js',
];

const GENERATED_ARTIFACTS = [
  'src/data/generatedStats.js',
  'src/data/generatedEvidenceReviewQueue.js',
  'src/data/generatedOpenTargetsSnapshot.js',
  'src/data/generatedOpenTargetsPromotionQueue.js',
  'src/data/generatedOpenTargetsReviewTargets.js',
  'src/data/generatedOpenTargetsPgxGapRoadmap.js',
  'src/data/generatedOpenTargetsMechanisticQueue.js',
  'docs/OPEN_TARGETS_INTEGRATION_AUDIT.md',
  'docs/OPEN_TARGETS_IDENTITY_REVIEW.md',
  'docs/OPEN_TARGETS_PROMOTION_QUEUE.md',
  'docs/OPEN_TARGETS_FIRST_REVIEW_TARGETS.md',
  'docs/OPEN_TARGETS_PGX_GAP_ROADMAP.md',
  'docs/OPEN_TARGETS_MECHANISTIC_REVIEW_QUEUE.md',
];

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function read(filePath) {
  return readFileSync(resolve(filePath), 'utf8');
}

function moduleOrder() {
  const build = read('build.js');
  const match = build.match(/const MODULE_ORDER = \[([\s\S]*?)\];/);
  assert(match, 'Could not find MODULE_ORDER in build.js');
  return [...match[1].matchAll(/'([^']+)'/g)].map(row => row[1]);
}

function hasGeneratedHeader(filePath, text) {
  if (filePath.endsWith('.md')) return /Generated by `scripts\//.test(text);
  return /Generated by scripts\/|Auto-generated by scripts\//.test(text);
}

const errors = [];
for (const artifact of GENERATED_ARTIFACTS) {
  const path = resolve(artifact);
  if (!existsSync(path)) {
    errors.push(`${artifact}: missing generated artifact`);
    continue;
  }
  const text = read(artifact);
  if (!hasGeneratedHeader(artifact, text)) {
    errors.push(`${artifact}: missing generated-file header`);
  }
}

const order = moduleOrder();
const positions = GENERATED_DATA_MODULES.map(module => order.indexOf(module));
for (let idx = 0; idx < GENERATED_DATA_MODULES.length; idx += 1) {
  if (positions[idx] < 0) errors.push(`build.js MODULE_ORDER missing ${GENERATED_DATA_MODULES[idx]}`);
}
if (positions.every(pos => pos >= 0)) {
  const sorted = [...positions].sort((a, b) => a - b);
  for (let idx = 1; idx < sorted.length; idx += 1) {
    if (sorted[idx] !== sorted[idx - 1] + 1) {
      errors.push('Generated data modules are not contiguous in build.js MODULE_ORDER');
      break;
    }
  }
  for (let idx = 0; idx < GENERATED_DATA_MODULES.length; idx += 1) {
    if (order[sorted[0] + idx] !== GENERATED_DATA_MODULES[idx]) {
      errors.push(`Generated data module order changed: expected ${GENERATED_DATA_MODULES[idx]} at generated block offset ${idx}`);
    }
  }
}

if (errors.length) {
  console.error(JSON.stringify({ ok: false, errors }, null, 2));
  process.exit(1);
}

console.log(JSON.stringify({
  ok: true,
  generatedArtifacts: GENERATED_ARTIFACTS.length,
  generatedDataModules: GENERATED_DATA_MODULES.length,
  generatedBlockStart: Math.min(...positions),
}, null, 2));
